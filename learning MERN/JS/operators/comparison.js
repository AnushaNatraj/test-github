// Comparision Operator

// Equqlity ==
// Not Equal !=
// Greater Than >
// Greater than Equal >=
// Less Than <
// Less Than Equal <=
// Strict Equality ===
// Strict Not Equal !==


var a =15 ;
var y = 46;

// Equality
console.log(a == y);

// Not equality
console.log(a != y);

// Greater Than

console.log(a>y);

// Greater than Equal

console.log(a>=y);

// less than

console.log(a<y);

// Less than Equal

console.log(a<=y);

// Strict Equality

console.log(a===y);

// Strict Not equal

console.log(a!==y);